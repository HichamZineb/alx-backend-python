#!/usr/bin/env python3
"""
Module with a type-annotated function to_str that
takes a float as an argument and returns its string representation.
"""


def to_str(n: float) -> str:
    """
    Function that returns the string representation of a given float.

    Args:
        n (float): The input float.

    Returns:
        str: The string representation of the input float.
    """
    return str(n)
