#!/usr/bin/env python3
"""
Module with a type-annotated function add that takes two floats
as arguments and returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """
    Function that adds two floats and returns their sum.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
